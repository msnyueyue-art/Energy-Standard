// 选择语言
function selectLanguage(lang) {
    currentLang = lang;
    setLanguage(currentLang);
    localStorage.setItem('language', currentLang);

    // 更新下拉菜单的激活状态（兼容两种class名称）
    document.querySelectorAll('.lang-option, .language-option').forEach(option => {
        option.classList.remove('active');
    });
    const activeOption = document.querySelector(`.lang-option[onclick="selectLanguage('${lang}')"], .language-option[onclick="selectLanguage('${lang}')"]`);
    if (activeOption) {
        activeOption.classList.add('active');
    }

    // 触发自定义事件，通知页面语言已切换
    const languageChangeEvent = new CustomEvent('languageChanged', {
        detail: { language: lang }
    });
    window.dispatchEvent(languageChangeEvent);

    // 关闭下拉菜单
    const dropdown = document.getElementById('languageDropdownMenu');
    if (dropdown) {
        setTimeout(() => {
            dropdown.style.display = 'none';
        }, 100);
    }
    // 兼容旧版下拉菜单
    const oldDropdown = document.getElementById('langDropdown');
    if (oldDropdown) {
        setTimeout(() => {
            oldDropdown.classList.remove('show');
        }, 100);
    }
}

// 艹！获取翻译文本的辅助函数，给动态生成的内容用
function getTranslation(key) {
    const t = translations[currentLang];
    return t && t[key] ? t[key] : key;
}

function setLanguage(lang) {
    const t = translations[lang];
    if (!t) return;

    // 更新所有翻译文本
    Object.keys(t).forEach(key => {
        const element = document.getElementById(key);
        if (element) {
            if (element.tagName === 'INPUT' && element.type === 'text') {
                element.placeholder = t[key];
            } else {
                element.textContent = t[key];
            }
        }
    });

    // 处理data-translate-placeholder属性的元素
    document.querySelectorAll('[data-translate-placeholder]').forEach(el => {
        const key = el.getAttribute('data-translate-placeholder');
        if (t[key]) {
            el.placeholder = t[key];
        }
    });

    // 处理data-translate属性的元素（用于option等元素）
    document.querySelectorAll('[data-translate]').forEach(el => {
        const key = el.getAttribute('data-translate');
        if (t[key]) {
            el.textContent = t[key];
        }
    });

    // 处理data-translate-title属性的元素（用于title属性翻译）
    document.querySelectorAll('[data-translate-title]').forEach(el => {
        const key = el.getAttribute('data-translate-title');
        if (t[key]) {
            el.title = t[key];
        }
    });

    // 处理data-station属性的元素（用于带容量信息的option）
    document.querySelectorAll('[data-station]').forEach(el => {
        const key = el.getAttribute('data-station');
        if (t[key]) {
            // 提取容量信息（括号及其内容）
            const originalText = el.textContent;
            const capacityMatch = originalText.match(/\(.*?\)/);
            const capacityInfo = capacityMatch ? ' ' + capacityMatch[0] : '';
            // 组合翻译的站点名称和原始的容量信息
            el.textContent = t[key] + capacityInfo;
        }
    });

    // 处理特殊的重复ID元素（比昨日）
    if (document.getElementById('compareDayText1')) {
        document.getElementById('compareDayText1').textContent = t.compareDayText;
    }
    if (document.getElementById('compareDayText2')) {
        document.getElementById('compareDayText2').textContent = t.compareDayText;
    }
    if (document.getElementById('compareDayText3')) {
        document.getElementById('compareDayText3').textContent = t.compareDayText;
    }

    // 处理充电量、放电量、收益的多个实例
    if (document.getElementById('chargeAmount1')) {
        document.getElementById('chargeAmount1').textContent = t.chargeAmount;
    }
    if (document.getElementById('dischargeAmount1')) {
        document.getElementById('dischargeAmount1').textContent = t.dischargeAmount;
    }
    if (document.getElementById('revenue1')) {
        document.getElementById('revenue1').textContent = t.revenue;
    }

    // 更新HTML语言属性
    document.documentElement.lang = lang === 'zh' ? 'zh-CN' : 'en-US';

    // 如果页面有renderTable函数，重新渲染表格
    if (typeof renderTable === 'function') {
        renderTable();
    }

    // 如果页面有renderAlarmTable函数，重新渲染告警表格
    if (typeof renderAlarmTable === 'function') {
        renderAlarmTable();
    }

    // 如果页面有updateDashboardTranslations函数，更新dashboard动态内容
    if (typeof updateDashboardTranslations === 'function') {
        updateDashboardTranslations();
    }

    // 如果页面有updateDevicesTranslations函数，更新devices动态内容
    if (typeof updateDevicesTranslations === 'function') {
        updateDevicesTranslations();
    }
}

